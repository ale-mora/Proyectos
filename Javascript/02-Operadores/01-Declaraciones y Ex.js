


/** En JavaScript, una declaración (declaration), una sentencia (statement) y una expresión (expression) tienen significados distintos:

1) Declaración (Declaration): Es cuando se introduce una variable o función en el entorno de ejecución. Por ejemplo, var x;
o function miFuncion() {} son declaraciones. La declaración de una función establece una función con un nombre y puede 
ser llamada antes de que se ejecute su código debido al hoisting.

(let, const, function, async function, class, export / import)


2) Sentencia (Statement): Es una instrucción que realiza una acción. Por ejemplo, una declaración de variable, un bucle, 
una condición if-else, o una declaración de función son todas sentencias. No producen un valor por sí mismas.

3)Expresión (Expression): Es cualquier unidad de código que resuelve en un valor. Por ejemplo, 5, 5 + 3, miFuncion(), 
o incluso x = 7 son expresiones porque cada una de ellas produce un valor.

En resumen, una declaración introduce una entidad nueva en tu código, una sentencia realiza una acción, y una expresión 
produce un valor. Las expresiones pueden ser parte de las sentencias, como cuando asignas un valor a una variable o cuando 
una función retorna un valor. */